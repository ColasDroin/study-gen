def install_cc_madx_sequence(mad: cpymad.madx.Madx) -> cpymad.madx.Madx:
    # Install crab cavities (they are off)
    mad.input("""
    call, file='acc-models-lhc/toolkit/enable_crabcavities.madx';
    on_crab1 = 0;
    on_crab5 = 0;
    """)

    return mad
